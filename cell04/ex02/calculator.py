#!/usr/bin/env python3
first_no = int(input("Give me the first number: "))
second_no = int(input("Give me the second number: "))
print("Thank you!")
print(f"{first_no} + {second_no} = {first_no + second_no}")
print(f"{first_no} - {second_no} = {first_no - second_no}")
print(f"{first_no} / {second_no} = {first_no / second_no}")
print(f"{first_no} * {second_no} = {first_no * second_no}")
